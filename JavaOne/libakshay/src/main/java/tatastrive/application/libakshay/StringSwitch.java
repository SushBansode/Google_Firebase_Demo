package tatastrive.application.libakshay;

public class StringSwitch {
    public static void main(String[] args) {
        String color="green";
        switch(color){
            case "Red":
                System.out.println("Red");
                break;
            case "yellow":
                System.out.println("yellow");
                break;

            default:
                System.out.println("Yooooooo");
        }
    }
}
