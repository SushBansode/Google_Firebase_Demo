package tatastrive.application.libswitch;

public class MyClassSwitch {
    public static void main(String[] args) {
        int day=2;
        switch (day)
        {
            case 1:
                System.out.println("monday");
                break;
            case 2:
                System.out.println("tue");
                break;
            default:
                System.out.println("Invalid noooooo");
        }
    }

}

